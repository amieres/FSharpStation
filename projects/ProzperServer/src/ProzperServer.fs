            let invokeE nameF (f:'a->Eff<'H, 'b>) (p:'a) =
                try 
                    f p |> Ok
                with e ->
                    Error (sprintf "Exception! %s %s" <| nameF p <| e.ToString())
                |> Rsl.ofResult
                |> join
        
            let invokeR nameF (f:'a->'b) (p:'a) =
                try 
                    f p |> Ok
                with e ->
                    Error (sprintf "Exception! %s %s" <| nameF p <| e.ToString())
